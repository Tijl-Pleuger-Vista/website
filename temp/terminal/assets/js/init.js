const viewportHeight = window.innerHeight;
const viewportW = window.innerWidth;
if (viewportHeight > viewportW) {
    window.open("https://tijl-pleuger-vista.github.io/website.github.io/terminal/warning")
}   else    {
    window.open("https://tijl-pleuger-vista.github.io/website.github.io/terminal/terminal")
}